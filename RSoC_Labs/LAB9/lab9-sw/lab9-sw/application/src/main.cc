// ============================================================
// MAIN FUNCTION
// ============================================================
int main()
{
    XIOModule iom;
    XIOModule_Initialize(&iom, XPAR_IOMODULE_0_DEVICE_ID);
    XIOModule_Start(&iom);

    // Enable timer (optional)
    XIOModule_Connect(&iom, XIN_IOMODULE_FIT_1_INTERRUPT_INTR, timerTick, NULL);
    XIOModule_Enable(&iom, XIN_IOMODULE_FIT_1_INTERRUPT_INTR);
    Xil_ExceptionInit();
    Xil_ExceptionRegisterHandler(XIL_EXCEPTION_ID_INT, (Xil_ExceptionHandler)XIOModule_DeviceInterruptHandler, NULL);
    Xil_ExceptionEnable();
    microblaze_enable_interrupts();

    // ============================================================
    // FONT 0: Mario Theme (addresses 0-255)
    // ============================================================
    for(unsigned i = 0; i < 256; i++)
    {
        XIOModule_IoWriteByte(&iom, i, mario_tune[i]);
    }

    // ============================================================
    // FONT 1: Happy Birthday (addresses 256-511)
    // ============================================================
    for(unsigned i = 0; i < 256; i++)
    {
        XIOModule_IoWriteByte(&iom, 256 + i, birthday_tune[i]);
    }

    // ============================================================
    // FONT 2: Mortal Kombat Theme (addresses 512-767)
    // ============================================================
    for(unsigned i = 0; i < 256; i++)
    {
        XIOModule_IoWriteByte(&iom, 512 + i, mk_theme[i]);
    }

    // ============================================================
    // FONT 3: Heartbeat / Fun Effect (addresses 768-1023)
    // ============================================================
    for(unsigned i = 0; i < 256; i++)
    {
        XIOModule_IoWriteByte(&iom, 768 + i, heartbeat[i]);
    }

    // Show "FUN" on display
    XIOModule_DiscreteWrite(&iom, 1, 0x46554E00);  // "FUN"

    while(1);
    return 0;
}